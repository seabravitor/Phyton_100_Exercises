x=input('escreva algo: ')
print('O tipo primitivo desse número é', type(x))
print('Só tem espaços?', x.isspace())
print('É Alfabético?', x.isalpha())
print('Está tudo em Maisculos?', x.isupper())
print('Está em Minusculo?',x.islower())
print('Está Capitalizada? Maiuscula e Minuscula?',x.istitle())

